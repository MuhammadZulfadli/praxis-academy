// Function Declaration
function aritmatika(a, b) {
  return a * b;
}
console.log(aritmatika(10, 2));

// Function Expression
let aritmatika2 = function (a, b) {
  return a + b;
};
console.log(aritmatika2(20, 2));

// Arrow function
let contoh = (a, b) => a + b;
console.log(contoh(2, 9));

const nama = () => {
  console.log("saya");
};
nama();
